#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*g;
	int		t;

	i = 0;
	t = 0;
	while (s[t] != '\0')
		t++;
	t++;
	while (t != i)
	{
		if ((s[i] != c))
			i++;
		else
		{
			g = (char *) &s[i];
			return (g);
		}
	}
	return (0);
}
